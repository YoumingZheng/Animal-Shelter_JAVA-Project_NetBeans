    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null,"please select a row first!");
            return;
        }
        AdoptionWorkRequest awr = (AdoptionWorkRequest)jTable.getValueAt(selectedRow, 0);
        if(jCheckBox1.isSelected()){
            awr.setValidId(Boolean.TRUE);
        }
        if(jCheckBox2.isSelected()){
            awr.setHadPet(Boolean.TRUE);
        }
        if(jCheckBox3.isSelected()){
            awr.setHavingPet(Boolean.TRUE);
        }
        awr.setFund(txtFund.getText());
        awr.setTime(txtTime.getText());
        awr.setHome(txtHome.getText());
        awr.setAllergy(txtAllergy.getText());
        txtFund.setText("");
        txtTime.setText("");
        txtHome.setText("");
        txtAllergy.setText("");
        JOptionPane.showMessageDialog(null, "Submit!");
    }//GEN-LAST:event_jButton4ActionPerformed

