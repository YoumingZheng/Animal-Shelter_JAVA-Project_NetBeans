    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable.getSelectedRow();
        AdoptionWorkRequest awr = (AdoptionWorkRequest)jTable.getValueAt(selectedRow, 0);
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null,"please select a row first!");
            return;
        }
        else{
            awr.setMessage(txtFeedback.getText());
            awr.setStatus(txtStatus.getText());            
            populateWorkRequest();
            txtFeedback.setText("");
            txtStatus.setText("");
        }
        JOptionPane.showMessageDialog(null, "Request Sent!");
        
        for(Organization o: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o instanceof InventoryOrganization){
             o.getAdoptionWorkQueue().getAdoptionWorkRequestList().add(awr);
            }
        }
        
        Animal an = awr.getAnimal();
        Customer adopter=awr.getAdopter();
        InspectionWorkRequest inswr = new InspectionWorkRequest();
        inswr.setSender(userAccount);
        inswr.setAnimal(an);
        inswr.setCustomer(adopter);
        inswr.setStatus("Generated");
        for(Organization o: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o instanceof InspectionOrganization){
                o.getInspectionWorkQueue().getWorkRequestList().add(inswr);
            }
        }
        if(inswr.getStatus().equalsIgnoreCase("Generated")){
            inswr.getCustomer().getUserAccount().getInspectionWorkQueue().getWorkRequestList().add(inswr);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

