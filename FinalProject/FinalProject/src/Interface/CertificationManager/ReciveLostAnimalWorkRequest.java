    private void populateWorkRequest() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest wr: organization.getLostAnimalWorkQueue().getWorkRequestList()){
            LostAnimalWorkRequest lawr = (LostAnimalWorkRequest)wr;
            Object[] row = new Object[3];
            row[0] = wr;
            row[1] = wr.getMessage();
            row[2] = wr.getStatus();
            
            
            
            
            
            model.addRow(row);
        }
    }
    
