    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
            LostAnimalWorkRequest lawr = (LostAnimalWorkRequest)jTable1.getValueAt(selectedRow, 0);

        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null,"please select a row first!");
            return;
        }
        else{
            lawr.setMessage(txtFeedback.getText());
            lawr.setStatus(txtStatus.getText());
            populateWorkRequest();
            txtFeedback.setText("");
            txtStatus.setText("");
        }
        JOptionPane.showMessageDialog(null, "Request Sent!");
        
        for(Organization o: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o instanceof InventoryOrganization){
             o.getLostAnimalWorkQueue().getWorkRequestList().add(lawr);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

