    public void populateTable(ArrayList<InspectionWorkRequest> list){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0);
        
        for (InspectionWorkRequest iwr : list){
            Object[] row = new Object[7];
            row[0] =iwr;
            row[1] = iwr.getCustomer().getAddress();
            row[2] = iwr.getCustomer().getId();
            row[3] = iwr.getFirstInspection();
            row[4] = (iwr.getSecondInspection() == null ? "waiting" : iwr.getSecondInspection());
            row[5] = (iwr.getThirdInspection() == null ? "waiting" : iwr.getThirdInspection());
            row[6] = iwr.getStatus();
            model.addRow(row);
        }
    }

