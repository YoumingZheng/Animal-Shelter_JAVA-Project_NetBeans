    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
         int threeC = 0;
        int twoC = 0;
        int threeA = 0;
        
        for(InspectionWorkRequest iwr:organization.getInspectionWorkQueue().getWorkRequestList()){
                if(iwr.getThirdInspection()!= null){
                    if(iwr.getFirstInspection().equals("C")
                       && iwr.getSecondInspection().equals("C")
                       && iwr.getThirdInspection().equals("C")){
                        threeC++;
                    }
                }
        }


        for(InspectionWorkRequest iwr:organization.getInspectionWorkQueue().getWorkRequestList()){
            int i=0;

             if(iwr.getSecondInspection()!= null){
                    if(iwr.getFirstInspection().equals("C")){
                        i++;
                    }
                    if(iwr.getSecondInspection().equals("C")){
                        i++;
                    }
                }
                    if(iwr.getThirdInspection() != null  ){
                        if(iwr.getThirdInspection().equals("C")){
                            i++;
                        }
                        
                    }


            if(i==2){
                twoC++;
            }
        }
        
        for(InspectionWorkRequest iwr:organization.getInspectionWorkQueue().getWorkRequestList()){
                int i=0;
                 if(iwr.getThirdInspection() != null){
                    if(iwr.getFirstInspection().equals("A")){
                    i++;
                }
                if(iwr.getSecondInspection().equals("A")){
                    i++;
                }
                if(iwr.getThirdInspection().equals("A")){
                    i++;
                }
                }
                
                if(i==3){
                    threeA++;
                }
            }
            
        DefaultPieDataset pieDataset = new DefaultPieDataset();
        pieDataset.setValue("Three C", threeC);
        pieDataset.setValue("Two C", twoC);
        pieDataset.setValue("Three A", threeA);
        JFreeChart chart = ChartFactory.createPieChart("Pie Chart", pieDataset, true, true, true);
        PiePlot p = (PiePlot)chart.getPlot();
        //p.setForegroundAlpha(TOP_ALIGNMENT);
        ChartFrame frame = new ChartFrame("Pie Chart",chart);
        frame.setVisible(true);
        frame.setSize(450,500);
    }//GEN-LAST:event_jButton8ActionPerformed


