    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
        // TODO add your handling code here:
        jButton4.setEnabled(true);
        list.clear();
        
        if(jRadioButton2.isSelected()){
            for(InspectionWorkRequest iwr:organization.getInspectionWorkQueue().getWorkRequestList()){
                    if(iwr.getThirdInspection()!= null){
                        if(iwr.getFirstInspection().equals("C")
                           && iwr.getSecondInspection().equals("C")
                           && iwr.getThirdInspection().equals("C")){
                            list.add(iwr);
                        }
                    }
            }
        }
        
        populateTable(list);
    }//GEN-LAST:event_jRadioButton2ActionPerformed

    private void jRadioButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton3ActionPerformed
        // TODO add your handling code here:
        list.clear();
        jButton4.setEnabled(true);
        
        if(jRadioButton3.isSelected()){
            for(InspectionWorkRequest iwr:organization.getInspectionWorkQueue().getWorkRequestList()){
                int i=0;
                
                
                if(iwr.getSecondInspection()!= null){
                    if(iwr.getFirstInspection().equals("C")){
                        i++;
                    }
                    if(iwr.getSecondInspection().equals("C")){
                        i++;
                    }
                }
                    if(iwr.getThirdInspection() != null  ){
                        if(iwr.getThirdInspection().equals("C")){
                            i++;
                        }
                        
                    }
                
                
        
                if(i==2){
                    list.add(iwr);
                }
            }
        }
        
        populateTable(list);
    }//GEN-LAST:event_jRadioButton3ActionPerformed

