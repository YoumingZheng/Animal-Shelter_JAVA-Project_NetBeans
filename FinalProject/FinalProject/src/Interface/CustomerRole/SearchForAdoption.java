    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        lalala.clear();
        
        for(Enterprise en:network.getEnterpriseDirectory().getEnterpriseList()){
            if(en instanceof AnimalShelterEnterprise){
                for(Animal animal:en.getAnimalDirectory().getAnimalDirectory()){
                    if((animal.getSpecies().equalsIgnoreCase(txtSSpecies.getText())) && (animal.getBreed().equalsIgnoreCase(txtSBreed.getText()))  ||  ((txtSSpecies.getText().equals("")) && (txtSBreed.getText().equals("")))){
                       if(animal.isStatus()) 
                           lalala.add(animal);
                    }
                }
            }
        }
        
        populateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Animal an=new Animal();
        //AnimalShelterEnterprise ase=new ;
        
        int selectedRow=jTable1.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please Select a Row First!","Warining",JOptionPane.WARNING_MESSAGE);
            return;
        }
        Animal an=(Animal)jTable1.getValueAt(selectedRow, 0);
        
        txtSpecies.setText(an.getSpecies());
        txtBreed.setText(an.getBreed());
        txtGender.setText(an.getGender());
        txtColor.setText(an.getColor());
        txtAge.setText(an.getAge());
        //jlPhoto.setIcon(an.getIcon());
        populateimage(an.getPicturepath(), jlPhoto);
        
        for(Enterprise ase:network.getEnterpriseDirectory().getEnterpriseList()){
            if(ase instanceof AnimalShelterEnterprise){
                for(Animal ani:ase.getAnimalDirectory().getAnimalDirectory()){
                    if(ani == an){
                        txtAddress.setText(ase.getAddress());
                        txtZipCode.setText(ase.getZipCode());
                        asee=(AnimalShelterEnterprise) ase;
                        break;
                    }
                }
            }
        }
        
        ann=an;
        
    }//GEN-LAST:event_jButton2ActionPerformed

